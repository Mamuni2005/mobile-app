let a=60;
let b=24;
switch(1)
{
    case 1:
        sum=a+b;
        console.log("sum="+sum);
        break;
    case 2:
        subtract=a-b;
        console.log("subtract="+subtract);
        break;
    case 3:
        multiplication=a*b;
        console.log("multiplication="+multiplication);
        break;
    case 4:
        division=a/b;
        console.log("division="+division);
        break;
    case 5:
         reminder=a%b;
         console.log("reminder="+reminder);
         break;
    default:
        console.log("invalid operation");
}